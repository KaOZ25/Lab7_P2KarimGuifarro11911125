public String user(String usuario){
    String user="";
    Duser ap=
                new Duser ("./usuarios.txt");     
    for (String t : ap.getLista()) {
        if (t.equals(usuario)) {
            usuario+= "1";
            user=usuario;
        }
    }
    return user;
}
